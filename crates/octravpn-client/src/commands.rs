// Helper so rand is referenced (for `KeyPair::generate` ergonomics in
// future provisioning paths).
#[allow(dead_code)]
fn fresh_secret_bytes() -> [u8; 32] {
    let mut buf = [0u8; 32];
    rand::rngs::OsRng.fill_bytes(&mut buf);
    buf
}
